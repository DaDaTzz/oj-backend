package com.da.oj.model.enums;

import org.apache.commons.lang3.ObjectUtils;

import java.util.Arrays;
import java.util.List;
import java.util.stream.Collectors;

/**
 * 判题信息消息枚举
 *
 * @author <a href="https://github.com/DaDaTzz">程序员Da</a>
 * @from <a href="https://github.com/DaDaTzz">程序员Da</a>
 */
public enum JudgeInfoMessageEnum {

    Accepted("成功", "Accepted"),
    Wrong_Answer("答案错误", "Wrong_Answer"),
    RunTime_Error("运行错误", "RunTime_Error"),
    Memory_Limit("内存溢出", "Memory_Limit"),
    Time_Limit("超时", "Time_Limit"),
    System_Error("系统错误", "System_Error"),
    Compile_Error("编译", "Compile_Error"),
    Dangerous_Operation("危险操作", "Dangerous_Error");


    private final String text;

    private final String value;

    JudgeInfoMessageEnum(String text, String value) {
        this.text = text;
        this.value = value;
    }

    /**
     * 获取值列表
     *
     * @return
     */
    public static List<String> getValues() {
        return Arrays.stream(values()).map(item -> item.value).collect(Collectors.toList());
    }

    /**
     * 根据 value 获取枚举
     *
     * @param value
     * @return
     */
    public static JudgeInfoMessageEnum getEnumByValue(String value) {
        if (ObjectUtils.isEmpty(value)) {
            return null;
        }
        for (JudgeInfoMessageEnum anEnum : JudgeInfoMessageEnum.values()) {
            if (anEnum.value.equals(value)) {
                return anEnum;
            }
        }
        return null;
    }

    public String getValue() {
        return value;
    }

    public String getText() {
        return text;
    }
}
